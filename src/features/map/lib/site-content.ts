export function isAuthoritySite(label: string | null | undefined): boolean {
  const n = label ?? "";
  return /استثمار/.test(n) && /هي[ئأ]ة|هيئه/.test(n); // نفس مطابقة STATIC_MODELS للهيئة
}

export function isSixTowerSite(label: string | null | undefined): boolean {
  const n = label ?? "";
  return /مجمع/.test(n) && /سكن/.test(n) && /(6|٦)/.test(n) && /(برج|أبراج|ابراج)/.test(n);
}

